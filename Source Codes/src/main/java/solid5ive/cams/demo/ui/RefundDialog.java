    private void refundButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refundButtonActionPerformed
        if (cardIdField.getText().isEmpty()) {
            return;
        }
        try {
            int reply = WebOptionPane.showConfirmDialog ( this, "Are you sure to refund?", "Confirmation", WebOptionPane.YES_NO_OPTION,
                        WebOptionPane.QUESTION_MESSAGE );
            if(reply != WebOptionPane.YES_OPTION) return;
            UserAccount account = userService.refundUserAccount((String) subject.getPrincipal(), cardIdField.getText());
            balanceLabel.setText(account.getBalance().toString());
            succeedLabel.setVisible(true);
            cardIdField.clear();
        } catch (IllegalArgumentException ex) {
            UiToolKits.showExceptionDialog(this, "Invalid request", ex);
        } catch (Exception ex) {
            UiToolKits.showExceptionDialog(this, "Unexpected exception", ex);
        }
    }

